export function seedContent(db: any): void {
  // Check if content_modules table already has data
  const row = db.prepare('SELECT COUNT(*) as cnt FROM content_modules').get();
  if (row && row.cnt > 0) {
    console.log(`[seed-content] content_modules already has ${row.cnt} rows — skipping.`);
    return;
  }

  const stmt = db.prepare(`
    INSERT INTO content_modules
      (category_id, title, icon_name, content, sort_order, warnings, alerts, links, status)
    VALUES
      (?, ?, ?, ?, ?, ?, ?, ?, 'published')
  `);

  let inserted = 0;
  for (const m of allModules) {
    stmt.run(
      m.category_id,
      m.title,
      m.icon_name,
      m.content,
      m.sort_order,
      m.warnings,
      m.alerts,
      m.links,
    );
    inserted++;
  }

  console.log(`[seed-content] Inserted ${inserted} content modules.`);
}
